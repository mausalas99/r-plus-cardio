import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const INCLUDE_RE = /<!--\s*@include\s+([^\s]+)\s*-->/g;
const GENERATED_BANNER = '<!-- generated by build-ui; edit index.src.html -->\n';

export function collectDuplicateIds(html) {
  const ids = [];
  const re = /\bid=["']([^"']+)["']/g;
  let m;
  while ((m = re.exec(html))) ids.push(m[1]);
  const seen = new Set();
  const dupes = [];
  for (const id of ids) {
    if (seen.has(id)) dupes.push(id);
    seen.add(id);
  }
  return dupes;
}

export function buildUi(srcPath) {
  const absSrc = path.resolve(srcPath);

  function expand(fileAbs, stack) {
    if (stack.includes(fileAbs)) {
      throw new Error(`include cycle: ${fileAbs}`);
    }
    if (!fs.existsSync(fileAbs)) {
      throw new Error(`include not found: ${path.basename(fileAbs)}`);
    }
    const nextStack = [...stack, fileAbs];
    const baseDir = path.dirname(fileAbs);
    const raw = fs.readFileSync(fileAbs, 'utf8');
    return raw.replace(INCLUDE_RE, (_, inc) => {
      return expand(path.resolve(baseDir, inc), nextStack);
    });
  }

  const content = expand(absSrc, []);
  const dupes = collectDuplicateIds(content);
  if (dupes.length) {
    throw new Error(`duplicate id attributes: ${dupes.join(', ')}`);
  }
  return GENERATED_BANNER + content;
}

const isMain = process.argv[1] === fileURLToPath(import.meta.url);
if (isMain) {
  const check = process.argv.includes('--check');
  const publicDir = path.join(process.cwd(), 'public');
  const src = path.join(publicDir, 'index.src.html');
  const dest = path.join(publicDir, 'index.html');
  if (!fs.existsSync(src)) {
    console.error('missing public/index.src.html');
    process.exit(1);
  }
  const built = buildUi(src);
  if (check) {
    const onDisk = fs.existsSync(dest) ? fs.readFileSync(dest, 'utf8') : '';
    if (onDisk !== built) {
      console.error('index.html out of date; run npm run build:ui');
      process.exit(1);
    }
    process.exit(0);
  }
  fs.writeFileSync(dest, built);
  console.log('wrote public/index.html');
}
