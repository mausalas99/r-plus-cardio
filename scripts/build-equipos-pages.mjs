#!/usr/bin/env node
/**
 * Sync canonical public/equipos → cloud/equipos-pages/public/equipos deploy mirror.
 * Run before Cloudflare Pages/Worker static deploy; excluded from VibeDrift (see .vibedriftignore).
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const SRC = path.join(ROOT, 'public', 'equipos');
const DEST = path.join(ROOT, 'cloud', 'equipos-pages', 'public', 'equipos');

const SKIP_NAMES = new Set(['.DS_Store', 'index.html', 'manifest.webmanifest', 'host-discovery.mjs']);

const CLOUD_HOST_DISCOVERY = `/**
 * Cloud deploy: API base is always the current origin (no LAN subnet probe).
 * Generated by scripts/build-equipos-pages.mjs — do not hand-edit.
 */
const PING_PATH = '/api/equipos/v1/ping';
const PROBE_TIMEOUT_MS = 5000;

export async function probeEquiposHost(base, signal) {
  const url = \`\${String(base || '').replace(/\\/+\$/, '')}\${PING_PATH}\`;
  const ctrl = new AbortController();
  const timer = setTimeout(() => ctrl.abort(), PROBE_TIMEOUT_MS);
  const onAbort = () => ctrl.abort();
  if (signal) {
    if (signal.aborted) {
      clearTimeout(timer);
      return null;
    }
    signal.addEventListener('abort', onAbort, { once: true });
  }
  try {
    const res = await fetch(url, { cache: 'no-store', signal: ctrl.signal });
    if (!res.ok) return null;
    const data = await res.json();
    return data && data.equipos ? String(base).replace(/\\/+\$/, '') : null;
  } catch (_e) {
    return null;
  } finally {
    clearTimeout(timer);
    if (signal) signal.removeEventListener('abort', onAbort);
  }
}

/** @param {{ signal?: AbortSignal }} [opts] */
export async function resolveEquiposApiBase(opts = {}) {
  const origin = typeof window !== 'undefined' ? window.location : null;
  if (!origin) return '';
  const base = \`\${origin.protocol}//\${origin.host}\`;
  const hit = await probeEquiposHost(base, opts.signal);
  return hit || base;
}

export function isLoopbackHostname() {
  return false;
}

export function normalizeHostOverride(raw) {
  return String(raw || '').trim().replace(/\\/+\$/, '');
}
`;

const CLOUD_MANIFEST = {
  id: '/',
  name: 'R+ Lista de espera',
  short_name: 'R+ Cola',
  description: 'Cola Lumify, EKG y Ultrasonido — R+',
  lang: 'es',
  start_url: '/',
  scope: '/',
  display: 'standalone',
  orientation: 'portrait',
  background_color: '#0f1419',
  theme_color: '#4a52e8',
  icons: [
    {
      src: '/equipos/icons/icon-192.png',
      sizes: '192x192',
      type: 'image/png',
      purpose: 'any',
    },
    {
      src: '/equipos/icons/icon-512.png',
      sizes: '512x512',
      type: 'image/png',
      purpose: 'any',
    },
    {
      src: '/equipos/icons/icon-512-maskable.png',
      sizes: '512x512',
      type: 'image/png',
      purpose: 'maskable',
    },
  ],
};

function copyRecursive(srcDir, destDir) {
  fs.mkdirSync(destDir, { recursive: true });
  for (const name of fs.readdirSync(srcDir)) {
    if (SKIP_NAMES.has(name)) continue;
    const src = path.join(srcDir, name);
    const dest = path.join(destDir, name);
    const st = fs.statSync(src);
    if (st.isDirectory()) {
      copyRecursive(src, dest);
      continue;
    }
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    fs.copyFileSync(src, dest);
  }
}

if (!fs.existsSync(SRC)) {
  console.error('missing canonical public/equipos');
  process.exit(1);
}

copyRecursive(SRC, DEST);
for (const stale of ['index.html']) {
  const p = path.join(DEST, stale);
  if (fs.existsSync(p)) fs.unlinkSync(p);
}
fs.writeFileSync(
  path.join(DEST, 'manifest.webmanifest'),
  `${JSON.stringify(CLOUD_MANIFEST, null, 2)}\n`
);
fs.writeFileSync(
  path.join(ROOT, 'cloud', 'equipos-pages', 'public', 'manifest.webmanifest'),
  `${JSON.stringify(CLOUD_MANIFEST, null, 2)}\n`
);
fs.writeFileSync(path.join(DEST, 'host-discovery.mjs'), CLOUD_HOST_DISCOVERY);

const swSrc = path.join(ROOT, 'public', 'equipos-sw.js');
const swDest = path.join(ROOT, 'cloud', 'equipos-pages', 'public', 'equipos-sw.js');
if (fs.existsSync(swSrc)) {
  fs.copyFileSync(swSrc, swDest);
}

const cloudIndex = path.join(ROOT, 'cloud', 'equipos-pages', 'public', 'index.html');
if (fs.existsSync(cloudIndex)) {
  const html = fs.readFileSync(cloudIndex, 'utf8');
  fs.writeFileSync(
    cloudIndex,
    html.replace(/equipos-app\.mjs\?v=cloud\d+/g, 'equipos-app.mjs?v=cloud16')
  );
}

console.log('synced public/equipos → cloud/equipos-pages/public/equipos (cloud manifest, host-discovery, no index.html)');
